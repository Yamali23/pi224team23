
public abstract class Calculations 
{
	private  String first_calc;
	private String second_calc;
	private  String third_calc;
	
    public void calc() {}
	
	public String getFirstCalc() 
	{
		return first_calc;
	}
	public String getSecondCalc() 
	{
		return second_calc;
	}
	public String getThirdCalc() 
	{
		return third_calc;
	}
	
	public void setFirstCalc (String first_calc)
	{
		this.first_calc = first_calc;
	}
	public void setSecondCalc (String second_calc)
	{
		this.third_calc = second_calc;
	}
	public void setThirdCalc (String third_calc)
	{
		this.third_calc = third_calc;
	}
}
